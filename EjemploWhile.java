public class EjemploWhile {
    public static void main(String[] args) {
        // While, para repetir instrucciones sin un final especifico
        
        int i = 1;
        while (i < 11) {
            System.out.println(i); // del 1 al 10
            i++;
        }

        int j = 10;
        while (j > 0) {
            System.out.println(j); // del 10 al 1
            j--;
        }
    }
}
